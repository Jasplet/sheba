!=======================================================================      
      subroutine scs_slw(dist,depth,slow)
!=======================================================================      
!
!     Calculate the slowness for an ScS phase. This is done using linear 
!     interpolation of a pre-calculated phase table (provided by the
!     subroutine scs_slw_table)
!
!     J.Wookey 2004
!
      implicit none
      real table(0:95,0:70) ! distance,depth
      integer idep,idis
      real dist,depth,slow,s
      real bilin
      external bilin
      character*5 sstr
      
!  ** load the ScS slowness table  
      call scs_slw_table(table)

      
!  ** calculate the indices
      idep = int(depth/10.0)
      idis = int(dist)
           
!  ** check whether the depth and distance are in range 
      if (idep.lt.0 .or. idep.gt.70 .or. idis.lt.0 .or. idis.gt.96) then
         print*,'SCS_SLW: Warning, depth/distance out of range'
         print*,'SCS_SLW: IDIS, IDEP',idis,idep
         pause 
      endif
      
      if (idep.lt.0) idep=0
      if (idep.gt.70) idep=70
      if (idis.lt.0) idis=0
      if (idis.gt.95) idis=95
      
!  ** if it is equal to the last index
      if (idep.eq.70) idep=69           
      if (idis.eq.95) idep=94
            
      s = bilin(dist, depth, real(idis), real(idis+1), & 
                                real(idep)*10., real(idep+1)*10., & 
                               table(idis,idep),table(idis+1,idep), & 
                               table(idis+1,idep+1),table(idis,idep+1))
      
!  ** truncate to 3 d.p.
      write(sstr,'(f5.3)') s
      read(sstr,*) slow
         
      return
      end subroutine scs_slw

!=======================================================================
      function bilin(xx,yy,x1,x2,y1,y2,v1,v2,v3,v4)
!=======================================================================
!
!     Perform a bilinear interpolation between 4 grid points
!
!     v1(x1,y1), v2(x2,y1), v3(x2,y2), v4(x1,y2)
!
!     return v(xx,yy)
!
!     J. Wookey 2004
!
      implicit none
      real bilin
      real xx,yy
      real v1,v2,v3,v4
      real x1,x2,y1,y2
      real t,u

      t=(xx-x1)/(x2-x1)
      u=(yy-y1)/(y2-y1)
         
      bilin = (1-t)*(1-u)*v1 + t*(1-u)*v2 + t*u*v3 + (1-t)*u*v4   
      
      return
      end function bilin
!=======================================================================

      
